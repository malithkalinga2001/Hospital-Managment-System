    private void DAEditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DAEditBtnMouseClicked
        if(DAIdTb.getText().isEmpty()|| DANameTb.getText().isEmpty() || DAPhoneTb.getText().isEmpty() || DAAddressTb.getText().isEmpty() ||DATypeCb.getSelectedIndex() == -1 || DAPasswordTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{
           try{
               stmt = DBConnection.getStatementConnection();
               String BId = DAIdTb.getText();
               String Query = "Update hospital_information.doctortbl set Name = '"+DANameTb.getText()+"',Phone = '"+DAPhoneTb.getText()+"',Address = '"+DAAddressTb.getText()+"', Type = '"+DATypeCb.getSelectedItem()+"',Password = '"+DAPasswordTb.getText()+"' where Doctor_ID = "+BId;
               stmt.executeUpdate(Query);
               
               JOptionPane.showMessageDialog(this, "Doctor Updated");
               DisplayDoctors();
               Reset();
           } 
           catch(Exception e){
               e.printStackTrace();
           }
        }
    }//GEN-LAST:event_DAEditBtnMouseClicked

    private void DADeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DADeleteBtnMouseClicked
        if(DAIdTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{
           try{
               stmt = DBConnection.getStatementConnection();
               String DId = DAIdTb.getText();
               String Query = "Delete from hospital_information.doctortbl where Doctor_ID="+DId;
               stmt.executeUpdate(Query);
               
               JOptionPane.showMessageDialog(this, "Doctor Deleted");
               DisplayDoctors();
               Reset();
           } 
           catch(Exception e){
               e.printStackTrace();
           }
        }
    }//GEN-LAST:event_DADeleteBtnMouseClicked

