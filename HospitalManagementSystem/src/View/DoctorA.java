    private void DASaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DASaveBtnActionPerformed
        
        if(DAIdTb.getText().isEmpty()|| DANameTb.getText().isEmpty() || DAPhoneTb.getText().isEmpty() || DAAddressTb.getText().isEmpty() ||DATypeCb.getSelectedIndex() == -1 || DAPasswordTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{
           
           try{
               stmt = DBConnection.getStatementConnection();
               
               Integer DId = Integer.valueOf(DAIdTb.getText());
               String DName = DANameTb.getText();
               String DPhone = DAPhoneTb.getText();
               String DAddress = DAAddressTb.getText();
               String DType = DATypeCb.getSelectedItem().toString();
               String DPassword = DAPasswordTb.getText();
               
               DoctorController.doctor(DId, DName, DPhone, DAddress, DType, DPassword);
               
               JOptionPane.showMessageDialog(this, "Book saved");
               
               DisplayDoctors();
               Reset();
               
               
           } 
           catch(Exception e){
               e.printStackTrace();
           }
        }
        
    }//GEN-LAST:event_DASaveBtnActionPerformed

